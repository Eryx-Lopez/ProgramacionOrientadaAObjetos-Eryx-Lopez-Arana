package edu.eryx.luciel.lopez.arana.reto1.process;

public class Calculadora {
    public static int sumarEnteros (int numero1, int numero2){
        return numero1 + numero1;
    }
    public static int restarEnteros (int numero1, int numero2){
        return numero1 - numero2;
    }
    public static int multiplicarEnteros (int numero1, int numero2){
        return numero1 * numero2;
    }
    public static int divisionEnteros (int numero1, int numero2){
        return numero1 / numero2;
    }
}
